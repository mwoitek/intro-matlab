## Lesson 1 Wrap-up

## As of early 2018, Usain Bolt holds the world record in the men's 100-meter dash.
## It is 9.58 seconds. What was his average speed in km/h? Assign the result to a
## variable called `hundred`.

hundred = 100 / 9.58;
hundred = hundred * 3.6

## Kenyan Eliud Kipchoge set a new world record for men of 2:01:39 on September 16,
## 2018. Assign his average speed in km/h to the variable `marathon`. The marathon
## distance is 42.195 kilometers.

distance_m = 42195;
time_s = 2 * 3600 + 60 + 39;
marathon = distance_m / time_s;
